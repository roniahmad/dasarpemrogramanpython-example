# %% A.12.2. Perulangan data list

list_1 = [10, 70, 20]
for e in list_1:
    print("elem:", e)

list_1 = [10, 70, 20]
for i in range(0, len(list_1)):
    print("index:", i, "elem:", list_1[i])

# %% â—‰ Fungsi `enumerate()`

list_1 = [10, 70, 20]
for i, v in enumerate(list_1):
    print("index:", i, "elem:", v)
