# A.12.4. List dari data range

# %% ◉ Konversi range ke list

range_1 = range(0, 10)
list_1 = list(range_1)
print(list_1)

range_2 = range(0, 22, 3)
list_2 = list(range_2)
print(list_2)

range_3 = range(100, 0, -10)
list_3 = list(range_3)
print(list_3)

# %% ◉ Konversi string ke list

alphabets = list('abcdefgh')
print(alphabets)

# %% ◉ Konversi tuple ke list

tuple_1 = (1, 2, 3, 4)
numbers = list(tuple_1)
print(numbers)
